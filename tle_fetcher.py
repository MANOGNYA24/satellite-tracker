def parse_tle_text(raw):
    lines = raw.strip().splitlines()
    satellites = []
    i = 0
    while i < len(lines) - 2:
        if not lines[i].startswith("1 ") and not lines[i].startswith("2 "):
            name = lines[i].strip()
            line1 = lines[i + 1]
            line2 = lines[i + 2]
            if line1.startswith("1 ") and line2.startswith("2 "):
                satellites.append({"name": name, "line1": line1, "line2": line2})
                i += 3
                continue
        i += 1
    return satellites


def load_satellites():
    try:
        url = "https://celestrak.org/NORAD/elements/gp.php?GROUP=stations&FORMAT=TLE"
        response = requests.get(url, timeout=10)
        response.raise_for_status()
        raw = response.text.strip()
        if "1 " in raw:
            sats = parse_tle_text(raw)
            if len(sats) > 0:
                print(f"Loaded {len(sats)} satellites from CelesTrak!")
                return sats
        raise ValueError("Bad response from CelesTrak")
    except Exception as e:
        print(f"CelesTrak unavailable ({e}), using built-in satellite data.")
        return parse_tle_text(FALLBACK_TLES)